<h2>Shopping Cart</h2>
<p>A shopping cart on an online store's site is a piece of software that enables visitors to add or remove items to their virtual basket. Moreover, the shopping cart enables consumers to review what they selected, make modifications or add extra items to their baskets if needed. To achieve this, the website needs to keep the record of the items a visitor has picked up from the online store until the visitor goes to the checkout page and makes a purchase. (Zand, 2019)</p>
<p>Shopping carts bridge the gap between shopping and buying, so having the best shopping cart software is extremely important on your website.</p>
<p>Some asset of shopping cart are as follows:</p>            
  
    <ul id="listul"> 
        <li>Purchase agreement</li>
        <li>Discount coupon </li>
        <li>Final submission order list.</li>
        <li>Last time remove item</li>
    </ul>
<p>Online shopping carts can be built with different coding languages including ASP, Java, JavaScript, and HTML. Each has its advantages and disadvantages, but an HTML shopping cart is more versatile and compatible with all browsers and devices. </p>            

<h3>Types of Shopping Carts: </h3> <p>(Best Shopping Cart Software for your Website)</p>

        <ol id="listol">
            <li>Open-source shopping cart software: Open-source software is released with an open license in which developers can access and edit the source code. An open source shopping cart had advantages and disadvantages. On one hand, the software is free and openly available. On the other hand, you might struggle to customize it since an open source shopping cart is not maintained by any one person or company.</li>
            <li>Licensed shopping cart software: Licensed software differs from open source. A single company develops licensed software and sells it. Moreover, the company is responsible with all developments and maintenance, although they may provide a method for external developers to customize it (through an API: application programming interface). Usually, there's a monthly or yearly subscription  fee for use of the software.</li>
            <li>Hosted eCommerce platforms: A hosted eCommerce platform (i.e., a SaaS solution) provides both shopping cart software and web hosting. Therefore, you can have your own web server as well as the software to meet your needs. As a result, you do not need to download and install the software yourself. In addition, the developers update and maintain the software. You can also run your business directly through a web browser since Hosted eCommerce platforms provide a web-based interface for you to manage your store.</li>   
        </ol>
      
